package Java_Programs2020;

public class NumbersSwapping {
public static void main(String[] args) {
    int a=5,b=6;
    System.out.println("Values of a and b before swapping   "+ a + ": "+ b);
    a=a+b;//11
    b=a-b;//5
    a=a-b;//6
    System.out.println("Values of a and b after swapping   "+ a + ": "+ b);
    
}
}
