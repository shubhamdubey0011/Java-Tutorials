/*•	The local variables will be created as part of the block execution in which it is declared and destroyed 
 * once that block execution completes. Hence the scope of the local variables is exactly same as scope of the 
 * block in which we declared.*/

package net.valtech.variable;

public class LocalVariables

{

	public static void main(String[] args) {
		int i = 0;
		for (int j = 0; j < 3; j++) 
		{
			i = i + j;
			
			
		}
		//
	}
	
}
